    private void jbtIzmenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtIzmenaActionPerformed
        lblPoruka.setText(" ");
        int r = jtblKorisnici.getSelectedRow();
        if (r < 0) {
            lblPoruka.setText("Morate prvo odabrati zaposlenog!");
            return;
        }
        Korisnik k = new Korisnik();
        k.setKorisnikID((int)jtblKorisnici.getValueAt(r, 0));
        k.setImePrezime(jtblKorisnici.getValueAt(r, 1).toString());
        k.setEmail(jtblKorisnici.getValueAt(r, 2).toString());
        k.setSifra(jtblKorisnici.getValueAt(r, 3).toString());
        k.setMesto((Mesto) jtblKorisnici.getValueAt(r, 4));
        k.setAdresa(jtblKorisnici.getValueAt(r, 5).toString());
        k.setUloga((int) jtblKorisnici.getValueAt(r, 7));

        try {
            TransferKlasa zahtev = new TransferKlasa();
            zahtev.setOperacija(Akcija.SACUVAJ_IZMENU_KORISNIKA);
            zahtev.setParametar(k);
            Kontroler.getINSTANCE().posalji(zahtev);
            TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
            JOptionPane.showMessageDialog(this, "Sistem je uspeo da sacuva izmenu zaposlenog", "Poruka", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtIzmenaActionPerformed

    private void jbtBrisanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtBrisanjeActionPerformed

        lblPoruka.setText(" ");
        int r = jtblKorisnici.getSelectedRow();
        if (r < 0) {
            lblPoruka.setText("Morate prvo odabrati zaposlenog!");
            return;
        }
        Korisnik k = new Korisnik();
        k.setKorisnikID((int) jtblKorisnici.getValueAt(r, 0));

        try {
            TransferKlasa zahtev = new TransferKlasa();
            zahtev.setOperacija(Akcija.BRISI_KORISNIKA);
            zahtev.setParametar(k);
            Kontroler.getINSTANCE().posalji(zahtev);
            TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
            JOptionPane.showMessageDialog(this, "Sistem je uspeo da obrise korisnika", "Poruka", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "GRESKA", JOptionPane.ERROR_MESSAGE);
            lblPoruka.setText("Nije moguce obrisati.");
            return;
        }
        try {
            TransferKlasa zahtev = new TransferKlasa();
            zahtev.setOperacija(Akcija.VRATI_SVE_KORISNIKE);
            Kontroler.getINSTANCE().posalji(zahtev);
            TransferKlasa odgovor = Kontroler.getINSTANCE().primi();
            List<Korisnik> zap = (List<Korisnik>) odgovor.getRezultat();
            srediTabelu(zap);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem nije uspeo da ucita korisnike.", "GRESKA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtBrisanjeActionPerformed

