    private void jtxtFilterKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtFilterKeyReleased
        String polje = jComboKriterijum.getSelectedItem().toString();
        String filter = jtxtFilter.getText().toLowerCase();
        List<Korisnik> listaTemp = new ArrayList<>();
        for (int i = 0; i < listaKorisnika.size(); i++) {
            listaTemp.add(i, listaKorisnika.get(i));
            
        }
        switch (polje) {
            
            case "KorisnikID":
                for (int i = listaKorisnika.size() - 1; i >= 0; i--) {
                    if (!(listaKorisnika.get(i).getKorisnikID() + "").contains(filter)) {
                        listaTemp.remove(i);
                    }
                }
                break;
            case "Ime i prezime":
                for (int i = listaKorisnika.size() - 1; i >= 0; i--) {
                    if (!listaKorisnika.get(i).getImePrezime().toLowerCase().contains(filter)) {
                        listaTemp.remove(i);
                    }
                }
                break;
            case "Email":
                for (int i = listaKorisnika.size() - 1; i >= 0; i--) {
                    if (!(listaKorisnika.get(i).getEmail()).contains(filter)) {
                        listaTemp.remove(i);
                    }
                }
                break;
            case "Sifra":
                for (int i = listaKorisnika.size() - 1; i >= 0; i--) {
                    if (!(listaKorisnika.get(i).getSifra()).contains(filter)) {
                        listaTemp.remove(i);
                    }
                }
                break;
            case "Mesto":
                for (int i = listaKorisnika.size() - 1; i >= 0; i--) {
                    if (!(listaKorisnika.get(i).getMesto().getNaziv().toLowerCase()).contains(filter)) {
                        listaTemp.remove(i);
                    }
                }
                break;
            case "Adresa":
                for (int i = listaKorisnika.size() - 1; i >= 0; i--) {
                    if (!(listaKorisnika.get(i).getAdresa().toLowerCase()).contains(filter)) {
                        listaTemp.remove(i);
                    }
                }
                break;
            case "Uloga":
                for (int i = listaKorisnika.size() - 1; i >= 0; i--) {
                    if (!(listaKorisnika.get(i).getUloga() + "").contains(filter)) {
                        listaTemp.remove(i);
                    }
                }
                break;
        }
        tmk.setListaKorisnika(listaTemp);
//        srediTabelu(listaKorisnika);
        //        JOptionPane.showMessageDialog(this, "Sistem je nasao zaposlene po zadatoj vrednosti", "Poruka", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jtxtFilterKeyReleased

